public class Car {

    private String name;
    private String model;
    private double price;
    private int quantity;

    Car(String name) {
        this.price = price;
        this.name = name;
        this.quantity = quantity;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public int getQuantity() {
        return quantity;
    }
}

